package Arrays;

public class Basic {
    public static void main(String args[]){
        int[] ia = new int[10];
        char[] ca = new char[10];
        String[] sa = new String[10];
        for(int i=0;i<10;i++){
            System.out.println("int "+ia[i]);
            System.out.println("char "+ca[i]);
            System.out.println("String "+sa[i]);
        }
    }    
}

